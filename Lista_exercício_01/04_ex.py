
# Coletar notas
n1 = float(input("Informe a 1ª nota: "))
n2 = float(input("Informe a 2ª nota: "))
n3 = float(input("Informe a 3ª nota: "))
n4 = float(input("Informe a 4ª nota: "))
print ("\n")

# Calcular média
media = (n1 + n2 + n3 + n4) / 4

# Exibir resultado da média
print(f"A média final é: {media}.")
