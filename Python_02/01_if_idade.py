# Variáveis 
nome = input("Samuel da Silva Izidoro: ")
idade = int(input("18: "))

# Verifica idade 
if idade >= 18:
    print(f"Olá, {nome}. Você tem {idade} anos.")
    print("Então, você é maior de idade.")
else:
    print(f"Olá, {nome}. Você tem {idade} anos.")
    print("Então, você é menor de idade.")
