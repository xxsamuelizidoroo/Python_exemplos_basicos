# Variáveis
nome = "Sabrina Capeater"
idade = 44

# Exibição de valores
print(nome)
print(idade)
print("\n")

# Método de concatenação
print("O seu nome é " + nome + " e você tem " + str(idade) + " anos.\n")

# 2º Método de concatenação (format)
print("O meu nome é {} e tenho {} anos.\n".format(nome, idade))

# 3º Método de concatenação (f-string)
print(f"O meu nome é {nome} e tenho {idade} anos.")
print("\n")

